#include <ctype.h>
/**
 * _isdigit - checks for digits
 *
 * @c: int c is the name of variable in function
 *
 * Return: 1 if c is a digit, otherwise returns 0 (Success)
 */


int _isdigit(int c)
{


	for (c = 32; c <= 126; c++)
{
	return (1);
}

	return (0);
}

